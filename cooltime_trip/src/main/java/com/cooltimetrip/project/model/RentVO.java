package com.cooltimetrip.project.model;

public class RentVO {
	private String rentNo;
	private String rentName;
	private String rentBusStop;
	private String rentItvTime;
	private String rentOpTime;
	private String rentTakenTime;
	private String rentPhoneNum;
	private String rentAddress;
	private String rentExpl;
	
	public String getRentNo() {
		return rentNo;
	}
	public void setRentNo(String rentNo) {
		this.rentNo = rentNo;
	}
	public String getRentName() {
		return rentName;
	}
	public void setRentName(String rentName) {
		this.rentName = rentName;
	}
	public String getRentBusStop() {
		return rentBusStop;
	}
	public void setRentBusStop(String rentBusStop) {
		this.rentBusStop = rentBusStop;
	}
	public String getRentItvTime() {
		return rentItvTime;
	}
	public void setRentItvTime(String rentItvTime) {
		this.rentItvTime = rentItvTime;
	}
	public String getRentOpTime() {
		return rentOpTime;
	}
	public void setRentOpTime(String rentOpTime) {
		this.rentOpTime = rentOpTime;
	}
	public String getRentTakenTime() {
		return rentTakenTime;
	}
	public void setRentTakenTime(String rentTakenTime) {
		this.rentTakenTime = rentTakenTime;
	}
	public String getRentPhoneNum() {
		return rentPhoneNum;
	}
	public void setRentPhoneNum(String rentPhoneNum) {
		this.rentPhoneNum = rentPhoneNum;
	}
	public String getRentAddress() {
		return rentAddress;
	}
	public void setRentAddress(String rentAddress) {
		this.rentAddress = rentAddress;
	}
	public String getRentExpl() {
		return rentExpl;
	}
	public void setRentExpl(String rentExpl) {
		this.rentExpl = rentExpl;
	}
	
}
