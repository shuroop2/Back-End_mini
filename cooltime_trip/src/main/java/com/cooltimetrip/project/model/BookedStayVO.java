package com.cooltimetrip.project.model;

public class BookedStayVO {
	private int booked_stay_no;
	private String memId;
	private String hotelNo;
	private String hotelName;
	private String roomType;
	private String roomView;
	private String roomPrice;
	private String daterange2;
	
	public int getBooked_stay_no() {
		return booked_stay_no;
	}
	public void setBooked_stay_no(int booked_stay_no) {
		this.booked_stay_no = booked_stay_no;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getHotelNo() {
		return hotelNo;
	}
	public void setHotelNo(String hotelNo) {
		this.hotelNo = hotelNo;
	}
	public String getHotelName() {
		return hotelName;
	}
	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}
	public String getRoomType() {
		return roomType;
	}
	public void setRoomType(String roomType) {
		this.roomType = roomType;
	}
	public String getRoomView() {
		return roomView;
	}
	public void setRoomView(String roomView) {
		this.roomView = roomView;
	}
	public String getRoomPrice() {
		return roomPrice;
	}
	public void setRoomPrice(String roomPrice) {
		this.roomPrice = roomPrice;
	}
	public String getDaterange2() {
		return daterange2;
	}
	public void setDaterange2(String daterange2) {
		this.daterange2 = daterange2;
	}
	
	
}

