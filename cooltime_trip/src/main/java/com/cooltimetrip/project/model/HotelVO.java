package com.cooltimetrip.project.model;

public class HotelVO {
	private String hotelNo;
	private String hotelName;
	private String hotelGrade;
	private String hotelAddress;
	private String hotelAssesment;
	private String hotelLocation;
	private String hotelContact;
	private String hotelRule;
	private String hotelTime;
	
	private int roomPrice;
	
	public int getRoomPrice() {
		return roomPrice;
	}
	public void setRoomPrice(int roomPrice) {
		this.roomPrice = roomPrice;
	}
	public String getHotelNo() {
		return hotelNo;
	}
	public void setHotelNo(String hotelNo) {
		this.hotelNo = hotelNo;
	}
	public String getHotelName() {
		return hotelName;
	}
	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}
	public String getHotelGrade() {
		return hotelGrade;
	}
	public void setHotelGrade(String hotelGrade) {
		this.hotelGrade = hotelGrade;
	}
	public String getHotelAddress() {
		return hotelAddress;
	}
	public void setHotelAddress(String hotelAddress) {
		this.hotelAddress = hotelAddress;
	}
	public String getHotelAssesment() {
		return hotelAssesment;
	}
	public void setHotelAssesment(String hotelAssesment) {
		this.hotelAssesment = hotelAssesment;
	}
	public String getHotelLocation() {
		return hotelLocation;
	}
	public void setHotelLocation(String hotelLocation) {
		this.hotelLocation = hotelLocation;
	}
	public String getHotelContact() {
		return hotelContact;
	}
	public void setHotelContact(String hotelContact) {
		this.hotelContact = hotelContact;
	}
	public String getHotelRule() {
		return hotelRule;
	}
	public void setHotelRule(String hotelRule) {
		this.hotelRule = hotelRule;
	}
	public String getHotelTime() {
		return hotelTime;
	}
	public void setHotelTime(String hotelTime) {
		this.hotelTime = hotelTime;
	}
	
	
}
