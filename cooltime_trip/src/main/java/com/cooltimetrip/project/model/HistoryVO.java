package com.cooltimetrip.project.model;

public class HistoryVO {
	public int historyNo;
	public String historyDep;
	public String historyArr;
	public String historyDateRange;
	public String historyShuttle;
	public String historyCount;
	public String historyType;
	public String memId;
	
	public int getHistoryNo() {
		return historyNo;
	}
	public void setHistoryNo(int historyNo) {
		this.historyNo = historyNo;
	}
	public String getHistoryDep() {
		return historyDep;
	}
	public void setHistoryDep(String historyDep) {
		this.historyDep = historyDep;
	}
	public String getHistoryArr() {
		return historyArr;
	}
	public void setHistoryArr(String historyArr) {
		this.historyArr = historyArr;
	}
	public String getHistoryDateRange() {
		return historyDateRange;
	}
	public void setHistoryDateRange(String historyDateRange) {
		this.historyDateRange = historyDateRange;
	}
	public String getHistoryShuttle() {
		return historyShuttle;
	}
	public void setHistoryShuttle(String historyShuttle) {
		this.historyShuttle = historyShuttle;
	}
	public String getHistoryCount() {
		return historyCount;
	}
	public void setHistoryCount(String historyCount) {
		this.historyCount = historyCount;
	}
	public String getHistoryType() {
		return historyType;
	}
	public void setHistoryType(String historyType) {
		this.historyType = historyType;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
}
